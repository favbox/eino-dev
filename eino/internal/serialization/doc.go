/*
Package serialization - Eino 框架的类型感知序列化系统

本包为 Eino 框架提供类型安全的序列化和反序列化能力，专门解决 JSON 序列化在强类型环境中的根本局限性。

核心问题：

	JSON 序列化会丢失 Go 语言的类型信息，特别是：
	  - 接口的具体实现类型无法恢复
	  - 结构体的精确类型信息丢失
	  - 多级指针的层级关系消失
	  - 动态类型的多态性无法保持

解决方案：

	在 JSON 数据基础上嵌入完整的类型元信息，实现：
	  • 类型信息的完整保存和恢复
	  • 接口多态性的精确保持
	  • 复杂嵌套结构的无损序列化
	  • 强类型环境下的状态持久化

核心特性：
  - 类型安全：双向类型映射确保序列化和反序列化的类型一致性
  - 接口多态：接口的具体实现类型能够精确恢复
  - 指针处理：支持多级指针的完整序列化和恢复
  - 复杂结构：递归处理任意深度的嵌套结构
  - 性能优化：基于 Sonic JSON 库的高性能数据编解码

应用场景：
 1. Compose 图状态管理 - 保存和恢复包含多种节点类型的图状态
 2. ADK 智能体检查点 - 持久化包含接口多态的智能体状态
 3. 组件状态传递 - 保持类型完整性的组件间数据传递

设计理念：
  - JSON+类型信息：在标准 JSON 基础上增加类型元数据
  - 反射驱动：基于 Go 反射机制的动态类型处理
  - 中间格式：通过 internalStruct 保持类型和数据的一致性
  - 泛型注册：类型安全的编译时检查和运行时映射
  - 智能赋值：自动处理类型转换、指针解引用和字段设置

使用对比：

	直接使用 Sonic：适合 API 通信，丢失类型信息
	使用本包：适合状态持久化，保持类型完整性

注意事项：
  - 需要序列化的类型必须先通过 GenericRegister 进行注册
  - 只序列化 struct 的导出字段，函数和 channel 会被忽略
  - 基础类型在 init() 函数中自动注册，用户类型需手动注册
*/
