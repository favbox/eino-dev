// Package embedding 提供了文本嵌入模型的核心接口和类型定义。
//
// 嵌入组件是 Eino 框架的重要组件之一，用于将文本转换为向量表示。
//
// 主要接口：
//   - Embedder：嵌入模型接口，定义了 EmbedStrings 方法用于文本向量化
//
// 设计特点：
//   - 简洁的同步 API 设计
//   - 支持批量文本嵌入
//   - 返回标准化的向量格式（[][]float64）
//   - 兼容主流嵌入模型 API
//
// 使用场景：
//   - 文档相似度计算和检索
//   - 语义搜索和向量数据库索引
//   - 文本聚类和分类
//   - 推荐系统和内容匹配
//   - RAG（检索增强生成）系统
//
// 典型使用流程：
//  1. 创建 Embedder 实例（对接 OpenAI、Azure、或其他嵌入服务）
//  2. 准备要嵌入的文本列表
//  3. 调用 EmbedStrings 方法
//  4. 处理返回的向量用于下游任务
